// وظيفة لتغيير اللغة
function changeLanguage(language) {
  // جلب اللغة الحالية من خاصية lang في العنصر <html>
  const currentLanguage = document.documentElement.getAttribute("lang");

  // إذا كانت اللغة المختارة هي نفس اللغة الحالية، لا نقوم بإعادة تحميل الصفحة
  if (language === currentLanguage) {
    return;
  }

  // تخزين اللغة المختارة في localStorage
  localStorage.setItem("language", language);

  // تحديد جميع العناصر التي تحتوي على data-key
  const elements = document.querySelectorAll("[data-key]");

  // تحديث النصوص بناءً على اللغة المختارة
  elements.forEach((element) => {
    const key = element.getAttribute("data-key");
    element.textContent = translations[language][key] || element.textContent;
  });

  // تحديث خاصية lang في العنصر <html>
  document.documentElement.setAttribute("lang", language);

  // تخزين اللغة في localStorage لتذكر الاختيار عند إعادة تحميل الصفحة
  localStorage.setItem("language", language);

  // تغيير اتجاه النصوص (rtl أو ltr) بناءً على اللغة
  if (language === "ar") {
    document.documentElement.setAttribute("dir", "rtl");
  } else {
    document.documentElement.setAttribute("dir", "ltr");
  }
}

// عند تحميل الصفحة، نحدد اللغة الافتراضية من localStorage
window.onload = function () {
  const savedLanguage = localStorage.getItem("language") || "en"; // اللغة الافتراضية هي الإنجليزية
  changeLanguage(savedLanguage);
};
